    import Foundation
    import UIKit

    extension UIColor {
        
        class func peachesYellow() -> UIColor {
            
            return UIColor(red: 239.0/255.0, green: 202.0/255.0, blue: 157.0/255.0, alpha: 1.0)
        }
        
        class func pastelGreen() -> UIColor {
            
            return UIColor(red: 174.0/255.0, green: 198.0/255.0, blue: 207.0/255.0, alpha: 1.0)
            
        }
        
        class func seaBlue() -> UIColor {
            return UIColor(red: 176.0/255.0, green: 196.0/255.0, blue: 222.0/255.0, alpha: 1.0)
        }
        
    }
